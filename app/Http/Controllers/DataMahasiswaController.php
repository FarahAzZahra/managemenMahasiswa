    {
        DB::table('tabel_mahasiswa')->insert(
            [
                'nama'=>$request->nama,
                'kelas'=>$request->kelas,
                'jurusan'=>$request->jurusan,
                'angkatan'=>$request->angkatan,
                'email' => $request->email, 
                'no_hp'=>$request->no_hp,
                'alamat'=>$request->alamat
                ]
        );
        return redirect('/')->with('status', 'Data success');
    }

